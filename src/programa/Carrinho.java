    private void btnAtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAtualizarActionPerformed
        
        tabelaProduto = (DefaultTableModel) tblProdutos.getModel();
        
        c = 0;
        int condicao = 0;
        
        if(limite == 0) {
            
            do {            
            
                if(nomesDosJogos[c] == null) {
                    condicao = 1;
                } else {
                    novoProduto = new String[ ] {nomesDosJogos[c], String.valueOf(precosDosJogos[c])};
                    tabelaProduto.addRow(novoProduto);
                    valorTotal = valorTotal + precosDosJogos[c];
                    c++;
                    valorDaCompra = String.valueOf(valorTotal); 
                    valorDaCompra = String.format("%.2f", valorTotal);
                    txtTotal.setText(valorDaCompra);
               
                }
            
            } while (condicao != 1 && c < 20);
            limite = 1;
        }
        
        
        
        
    }//GEN-LAST:event_btnAtualizarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed

        int r = tblProdutos.getSelectedRow();

        if(r == -1) {
            JOptionPane.showMessageDialog(null, "Selecione uma linha pra poder apagar");
        } else {
            String sValorDoItenDeletado = (String) tabelaProduto.getValueAt(r, 1);
            float fValorDoItenDeletado = Float.parseFloat(sValorDoItenDeletado);
            valorTotal = valorTotal - fValorDoItenDeletado;
            valorDaCompra = String.valueOf(valorTotal); 
            valorDaCompra = String.format("%.2f", valorTotal);
            txtTotal.setText(valorDaCompra);
            tabelaProduto.removeRow(r);
            nomesDosJogos[r] = null;
            precosDosJogos[r] = 0;
        }
        
    }//GEN-LAST:event_btnExcluirActionPerformed

    private void btnPagarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPagarActionPerformed
        
      compra.setVisible(true);
      compra.setTitle("Pagamento");
      compra.setLocationRelativeTo(null);
      compra.setValorAPagar(valorDaCompra);
      
      int condicao = 0; 
      c = 0;
      
      do {            
            
            if(nomesDosJogos[c] == null) {
                condicao = 1;
            } else {
                compra.nomesDosJogos[c] = nomesDosJogos[c];
                compra.precosDosJogos[c] = precosDosJogos[c];
            }
            
            c++;
            
        } while (condicao != 1 || c < 20 );
      
      compra.email = email;
      compra.senha = senha;
      compra.setNomeDoJogo(null);
        
    }//GEN-LAST:event_btnPagarActionPerformed

    private void btnHomeMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnHomeMouseClicked
        TelaPrincipal telaPrincipal = new TelaPrincipal();
        telaPrincipal.setVisible(true);
        telaPrincipal.setLocationRelativeTo(null);
        telaPrincipal.setTitle("Tela Principal");

        for (int c = 0; c < nomesDosJogos.length; c++) {
            telaPrincipal.jogos[c] = nomesDosJogos[c];
            telaPrincipal.precos[c] = precosDosJogos[c];
        }
        telaPrincipal.email = email;
        telaPrincipal.senha = senha;
        dispose();
    }//GEN-LAST:event_btnHomeMouseClicked

